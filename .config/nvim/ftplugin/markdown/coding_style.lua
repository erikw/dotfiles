-- Use spaces for indentation. It's sad, but some langauges or codebases wants this :'(.
vim.opt_local.expandtab = true		-- Indent with spaces.
vim.opt_local.tabstop = 2		-- Bullet list indentation
vim.opt_local.shiftwidth = 2		-- Tab width for auto indent and >> shifting.
vim.opt_local.softtabstop = 2		-- Number of spaces to act on for editing actions like BS and tab.
vim.opt_local.smarttab = true		-- Indent with spaces at beginning of line.
vim.opt_local.shiftround = true		-- Round indentation to nearest multiple of shiftwidth with <,>.
